  let current = 'ru'

  function detect() {
    const nav = (navigator.language || 'en').slice(0, 2).toLowerCase()
    return DICT[nav] ? nav : 'en'
  }

  function t(key) {
    return (DICT[current] && DICT[current][key]) || DICT.en[key] || key
  }

  function setLang(code) {
    current = DICT[code] ? code : 'en'
    document.documentElement.lang = current
    apply()
  }

  // Проставляет переводы всем элементам с data-i18n / data-i18n-aria
  function apply() {
    for (const el of document.querySelectorAll('[data-i18n]')) {
      el.textContent = t(el.dataset.i18n)
    }
    for (const el of document.querySelectorAll('[data-i18n-aria]')) {
      el.setAttribute('aria-label', t(el.dataset.i18nAria))
    }
  }

  window.I18N = { LANGUAGES, t, setLang, apply, detect, get lang() { return current } }
